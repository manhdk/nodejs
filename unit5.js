// If statement
var score = 6

// If statement
if (score > 5) {
    console.log("You're passed!")
}

// If... else statement
if (score > 7) {
    console.log("You're passed!")
} else {
    console.log("You're failed!")
}

// If...else if statement
if (score < 6) {
    console.log("Smaller")
} else if (score == 6) {
    console.log("Equal")
} else {
    console.log("Bigger")
}
