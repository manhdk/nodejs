var a = 10;
var b = 20;

// Arithmetic Operators
console.log("a + b = " + (a + b));
console.log("a + b = " + (a - b));
console.log("a * b = " + (a * b));
console.log("a / b = " + (a / b));
console.log(" ++a = " + (++a));
console.log("--a = " + (--a));

// Comparison Operators
console.log("a > b is " + (a > b));
console.log("a < b is " + (a < b));
console.log("a == b is " + (a == b));
console.log("a != b is " + (a != b));
console.log("a >= b is " + (a >= b));
console.log("a <= b is " + (a <= b));

// Assignment Operators
console.log("Value of a = " + (a = b))
console.log("Value of a = " + (a += b))
console.log("Value of a = " + (a -= b))
console.log("Value of a = " + (a *= b))
console.log("Value of a = " + (a /= b))
console.log("Value of a = " + (a %= b))

// Logical Operators
a = true;
b = false;
console.log("a AND b => " + (a && b));
console.log("a OR b => " + (a || b));
console.log("a NOT b => " + !(a && b));
